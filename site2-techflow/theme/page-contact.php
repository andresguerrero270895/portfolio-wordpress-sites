<script>
document.addEventListener('DOMContentLoaded', function () {

  const form      = document.getElementById('contactForm');
  const submitBtn = document.getElementById('submitBtn');
  const btnText   = document.querySelector('.tf-contact__submit-text');
  const btnLoad   = document.querySelector('.tf-contact__submit-loading');
  const success   = document.getElementById('formSuccess');
  const errorMsg  = document.getElementById('formError');

  // ── Pre-select service from URL param ────────────────────
  const urlParams = new URLSearchParams(window.location.search);
  const service   = urlParams.get('service');
  if (service) {
    const sel = document.getElementById('cf-service');
    if (sel) sel.value = service;
  }

  // ── Validation ────────────────────────────────────────────
  function validate() {
    let valid = true;

    const name  = document.getElementById('cf-name');
    const email = document.getElementById('cf-email');
    const msg   = document.getElementById('cf-message');

    document.querySelectorAll('.tf-form-error').forEach(e => e.textContent = '');
    document.querySelectorAll('.tf-form-group input, .tf-form-group textarea')
      .forEach(el => el.classList.remove('tf-input--error'));

    if (!name.value.trim()) {
      document.getElementById('err-name').textContent = 'Name is required';
      name.classList.add('tf-input--error');
      valid = false;
    }

    const emailReg = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    if (!email.value.trim() || !emailReg.test(email.value)) {
      document.getElementById('err-email').textContent = 'Valid email is required';
      email.classList.add('tf-input--error');
      valid = false;
    }

    if (!msg.value.trim() || msg.value.trim().length < 20) {
      document.getElementById('err-message').textContent = 'Please describe your project (min. 20 chars)';
      msg.classList.add('tf-input--error');
      valid = false;
    }

    return valid;
  }

  // ── Submit ────────────────────────────────────────────────
  form.addEventListener('submit', function (e) {
    e.preventDefault();
    if (!validate()) return;

    // Loading state
    btnText.style.display = 'none';
    btnLoad.style.display = 'inline-flex';
    submitBtn.disabled    = true;
    success.style.display = 'none';
    errorMsg.style.display = 'none';

    const data = new FormData();
    data.append('action',  'tf_contact');
    data.append('nonce',   tfAjax.nonce);
    data.append('name',    document.getElementById('cf-name').value);
    data.append('email',   document.getElementById('cf-email').value);
    data.append('company', document.getElementById('cf-company').value);
    data.append('service', document.getElementById('cf-service').value);
    data.append('budget',  document.getElementById('cf-budget').value);
    data.append('message', document.getElementById('cf-message').value);

    fetch(tfAjax.url, { method: 'POST', body: data })
      .then(r => r.json())
      .then(res => {
        btnText.style.display  = 'inline';
        btnLoad.style.display  = 'none';
        submitBtn.disabled     = false;

        if (res.success) {
          success.style.display  = 'flex';
          form.reset();
        } else {
          errorMsg.style.display = 'flex';
        }
      })
      .catch(() => {
        btnText.style.display  = 'inline';
        btnLoad.style.display  = 'none';
        submitBtn.disabled     = false;
        errorMsg.style.display = 'flex';
      });
  });

  // ── AOS ───────────────────────────────────────────────────
  const aosEls = document.querySelectorAll('[data-aos]');
  const obs = new IntersectionObserver((entries) => {
    entries.forEach(e => {
      if (e.isIntersecting) {
        setTimeout(() => e.target.classList.add('aos-animate'),
          parseInt(e.target.dataset.aosDelay || 0));
        obs.unobserve(e.target);
      }
    });
  }, { threshold: 0.1 });
  aosEls.forEach(el => obs.observe(el));

});
</script>

